package main

// server.go owns the bare-`stax` local HTTP server. The server is
// loopback-only (serverListenAddr is 127.0.0.1:7829) so it never
// accepts connections from elsewhere on the network — this is a per-
// user CLI assistant, not a shared service. The handlers below speak
// the small JSON API that ships with stax today:
//
//   GET /api/hello   — liveness / capability probe; returns the running
//                      stax version so a UI can confirm the binary it's
//                      talking to.
//   GET /api/systems — reads .stax/_data_systems.yaml from the current
//                      working directory and returns its parsed
//                      systems registry as a JSON array. Missing file
//                      or missing project marker → 200 with an empty
//                      array (no project in cwd is a normal state, not
//                      an error worth a non-2xx).
//
// The handler / mux / lifecycle split makes the routes unit-testable
// via httptest.NewRecorder while runServer owns the listen-loop +
// signal-handling shell that is only meaningful in the real CLI.

